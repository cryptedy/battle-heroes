<template>
  <BaseButton v-if="canDeleteBattle" type="danger" @click="deleteBattle">
    <template v-if="type === 'simple'">
      <FontAwesomeIcon icon="xmark" />
      バトル
    </template>
    <template v-else> バトル削除 </template>
  </BaseButton>
</template>

<script>
import { mapGetters, mapActions } from 'vuex'
import { NOTIFICATION_TYPE } from '@/utils/constants'

export default {
  name: 'BattleDeleteButton',

  props: {
    type: {
      type: String,
      required: false,
      default: 'default',
      validator: value => ['default', 'simple'].indexOf(value) !== -1
    },

    battle: {
      type: Object,
      required: true
    }
  },

  computed: {
    ...mapGetters({
      player: 'game/player',
      playerBattle: 'game/playerBattle'
    }),

    isPlayerBattle() {
      return this.playerBattle && this.playerBattle.id === this.battle.id
    },

    canDeleteBattle() {
      return this.playerBattle && this.isPlayerBattle
    }
  },

  methods: {
    ...mapActions({
      addNotification: 'notification/add'
    }),

    deleteBattle() {
      this.$socket.emit(
        'battle:delete',
        this.playerBattle.id,
        ({ status, message }) => {
          if (status) {
            this.addNotification({
              message,
              type: NOTIFICATION_TYPE.SUCCESS
            })
          } else {
            this.addNotification({
              message,
              type: NOTIFICATION_TYPE.ERROR,
              timeout: 0
            })
          }
        }
      )
    }
  }
}
</script>
